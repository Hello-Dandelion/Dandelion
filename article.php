			<h2>要多有钱才能去旅行</h2>
			<p class="msg"><span class="author">作者：<b class="name">陈小哇</b></span>&nbsp;&nbsp;<span class="time">更新时间：<time>2016-12-27</time></span></p>
			<p class="hr"></p>
			<div class="content">
				<img src="img/v0.jpg"/>
				<p>“别人都以为你很有钱。”我的闺蜜不止一次这么跟我说。</p>
				<p>她口中的“别人”，不乏有车有楼、身家数倍于我的企业高管、职场精英和阔太。他们眼看着我隔三岔五出国旅行，花上万元购置潜水装备，最多时囤了十几张机票，便觉得我是个小富婆。是的，我还晒过几百万的信用卡刷卡记录， 引起朋友圈一阵惊呼，其实，那是印尼盾，相当于人民币一千多块而已。</p>
				<p>究其原因，大概是他们认为要很有钱很有钱才能去旅行吧？那么问题来了，到底要多有钱才能去旅行？</p>
				<p>如果你有一两千块，足以来一趟一周左右的国内游。搭火车硬座和大巴，住青旅多人间，吃路边摊，我学生时代的旅行都是这么过来的。没花家里的钱，靠着每月几百块的兼职收入，也去了十多个省。</p>
				<p>如果你有四五千块，足以去东南亚和印度玩 10 天以上。秒杀廉航大促机票，睡机场，搭红眼航班，东南亚的消费水平其实比国内许多大城市低。一位闺蜜听说我在印度旅行一个月，包括疯狂购物，才花了3000 元，大为惊叹：“我在广州都不止花这么点！”</p>
				<p>然而，对一些人而言，像我这样的穷游算不上什么旅行。“没钱就不要出去浪荡，旅行是享受， 不是遭罪！”</p>
				<p>对一些有品位的人而言，旅行不光是享受，还是体验异国他乡最美好的东西，比如红酒、鹅肝、鱼子酱，比如躺在冰酒店里看极光。如果因为预算紧张而错过这些精髓，还不如不去。</p>
				<p>正如几年前在北京看赖声川的《这一夜，我们在旅途中说相声》，两个男人，一个奢华游一个背包客，都觉得自己的旅行最有意义，都瞧不起对方，富游的鄙视穷游的没见过好东西，不知道好酒店好餐厅的味道，穷游的鄙视富游的太肤浅太拜金，体会不到用双脚丈量大地的身心震撼（具体台词不记得，大概是这意思）。</p>
				<p>说到底，旅行到底要有多少钱，要怎样花才算花得值，完全在于每个人的消费观乃至人生观。谁有权定义旅行的意义，谁又有资格指责别人的旅行不是“真正的旅行”？</p>
				<p>如今，我不再追求花最少的钱去最远的地方，不再以同样的旅途，花得比别人少为傲，如果旅行前连日加班，我也愿意花一点儿钱住机场酒店，让自己养精蓄锐投入度假。我学会聪明地花钱，密切关注特价机票，避开旅游旺季，同时把钱花在自己最在乎的地方。我可以为了省几欧元车票步行很远，但我舍得花几十美元在百老汇看一场《悲惨世界》;我可以搭红眼航班，睡机场，但我舍得花上万元去潜水。如果我有更多的钱，我也想住豪华酒店，但我现在没有，因为那还不是我最在乎的。</p>
				<p>为什么你没有钱旅行？不是你真的比我穷，只是你觉得不值得，你觉得买一件看得见、摸得着、晒得出手的名牌，比潜到海里呼吸几口空气，看一些鱼还不能摸不能吃更值得，如此而已。每个人都是把时间和钱花在自己最在乎的地方，这就叫“任性”。不不，我才不会劝你去旅行呢，请坚持己见。</p>
				<p>最后，我真希望江湖上那些关于我很有钱的传说都是真的。</p>
