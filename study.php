			<p class="think_con">有甲、乙两人，其中，甲只说假话，而不说真话；乙则是只说真话，不说假话。但是，
				他们两个人在回答别人的问题时，只通过点头与摇头来表示，不讲话。有一天，一个人面对两条路：A与B，
				其中一条路是通向京城的，而另一条路是通向一个小村庄的。这时，他面前站着甲与乙两人，但他不知道此人是甲还是乙，
				也不知道“点头”是表示“是”还是表示“否”。现在，他必须问一个问题，才可能断定出哪条路通向京城。
				那么，这个问题应该怎样问？</p>
			<p class="answer">查看答案</p>
			<div class="think_answer">
				<p>参考答案:</p>
				<p>这个人只要站在A与B任何一条路上，然后，对着其中的一个人问：
					“如果我问他（甲、乙中的另外一个人）这条路通不通向京城，他会怎么回答？”</p>
				<p>如果甲与乙两个人都摇头的话，就往这条路向前走去，如果都点头，就往另一外一条走去。</p>
